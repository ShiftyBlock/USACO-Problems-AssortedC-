void setIO(string name, int submit) {
    if (submit) {
        ios_base::sync_with_stdio(0);
        cin.tie(0);
        freopen((name + ".in").c_str(), "r", stdin);
        freopen((name + ".out").c_str(), "w", stdout);
    }
    else{
        ios_base::sync_with_stdio(0);
        cin.tie(0);
    }

}

int main() {
    setIO("", 0);
    int tcs; cin>>tcs;
    while(tcs-->0){
        long N, M; cin>>N>>M;
        for(int i=2; i<=N; i++){
            if(N%i==0){
                N+=i;
                break;
            }
        }
        M--;
        N+=M*2;
        cout<<N<<endl;
    }
    return 0;
}

